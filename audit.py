def build_audit_prompt(repo_url: str, owner: str, repo_name: str) -> str:
    """
    Build the prompt that instructs Hermes Agent to perform the technical audit.

    Hermes Agent uses this prompt to plan its analysis, fetch repository data
    using its built-in web and browser tools, and generate a structured report.

    Args:
        repo_url: Full GitHub repository URL
        owner: Repository owner/organization name
        repo_name: Repository name

    Returns:
        A detailed prompt string for Hermes Agent
    """
    timestamp = datetime.now(timezone.utc).strftime("%Y-%m-%d %H:%M UTC")

    return f"""You are a senior software engineer and technical auditor.

Your task is to analyze the GitHub repository at: {repo_url}

Use your available tools to:
1. Fetch the repository page at {repo_url}
2. Analyze the README, file structure, and codebase
3. Identify the tech stack, code quality indicators, and potential risks

Then generate a complete technical audit report in Markdown format with EXACTLY these sections:

---

# Technical Audit Report: {owner}/{repo_name}

**Generated:** {timestamp}
**Tool:** repo-audit-agent v{VERSION} powered by Hermes Agent
**Repository:** {repo_url}

## Executive Summary

[2-3 paragraphs describing the project purpose, maturity, and overall health]

## Tech Stack

[List all detected languages, frameworks, tools, and infrastructure]

## Code Quality Score

**Score: X/10**

[Justify the score with specific observations about code organization,
documentation, test coverage, and maintainability]

## Risk Register (Top 5)

| # | Risk | Severity | Impact |
|---|------|----------|--------|
| 1 | ... | High/Medium/Low | ... |
| 2 | ... | High/Medium/Low | ... |
| 3 | ... | High/Medium/Low | ... |
| 4 | ... | High/Medium/Low | ... |
| 5 | ... | High/Medium/Low | ... |

[Explain each risk in detail]

## Improvement Roadmap (Top 5)

1. **[Priority name]** — [Specific, actionable improvement with expected impact]
2. **[Priority name]** — [Specific, actionable improvement with expected impact]
3. **[Priority name]** — [Specific, actionable improvement with expected impact]
4. **[Priority name]** — [Specific, actionable improvement with expected impact]
5. **[Priority name]** — [Specific, actionable improvement with expected impact]

## Conclusion

[Final assessment and recommended next steps]

---
*Report generated by repo-audit-agent using Hermes Agent (Nous Research)*

---

Output ONLY the Markdown report above. No additional commentary."""


def run_hermes_audit(repo_url: str, max_turns: int = 15, timeout: int = 300) -> str:
    """
    Invoke Hermes Agent to perform the repository audit.

    Hermes Agent is the core of this tool — it uses its agentic capabilities
    (web browsing, tool use, multi-step reasoning) to fetch and analyze the
    repository, then generates the structured report.

    Args:
        repo_url: GitHub repository URL to analyze
        max_turns: Maximum tool-calling iterations for Hermes Agent
        timeout: Maximum seconds to wait for completion

    Returns:
        The generated audit report as a Markdown string

    Raises:
        subprocess.TimeoutExpired: If Hermes Agent exceeds the timeout
        RuntimeError: If Hermes Agent returns an error
    """
    # Parse owner and repo name from URL
    parts = repo_url.rstrip("/").split("/")
    owner = parts[-2]
    repo_name = parts[-1]

    prompt = build_audit_prompt(repo_url, owner, repo_name)

    print(f"🔍 Analyzing repository: {repo_url}")
    print(f"⏳ Hermes Agent is working (this may take 2-3 minutes)...")
    print("   Hermes will browse the repository, analyze the codebase,")
    print("   and generate a comprehensive technical audit report.\n")

    result = subprocess.run(
        [
            "hermes", "chat",
            "--query", prompt,
            "--quiet",                    # Suppress banners and spinners
            "--max-turns", str(max_turns),
        ],
        capture_output=True,
        text=True,
        timeout=timeout,
    )

    # Combine stdout and stderr for full output
    raw_output = result.stdout + result.stderr

    # Filter out Hermes system messages to get clean Markdown
    clean_lines = [
        line for line in raw_output.split("\n")
        if not line.startswith("session_id:")
        and "tirith security scanner" not in line
        and "rgb:" not in line
        and "^[" not in line
    ]

    report = "\n".join(clean_lines).strip()

    if not report or "API call failed" in report or "Error:" in report:
        raise RuntimeError(
            f"Hermes Agent returned an error:\n{report[:500]}"
        )

    return report


# ---------------------------------------------------------------------------
# Output Handling
# ---------------------------------------------------------------------------

def save_report(report: str, repo_name: str, output_dir: Path) -> Path:
    """
    Save the audit report to a Markdown file.

    Args:
        report: The Markdown report content
        repo_name: Repository name (used in filename)
        output_dir: Directory where reports are saved

    Returns:
        Path to the saved report file
    """
    output_dir.mkdir(parents=True, exist_ok=True)

    timestamp = datetime.now().strftime("%Y%m%d_%H%M%S")
    filename = f"audit_{repo_name}_{timestamp}.md"
    filepath = output_dir / filename

    filepath.write_text(report, encoding="utf-8")

    return filepath


def print_summary(report: str, filepath: Path) -> None:
    """Print a summary of the audit results to stdout."""
    lines = report.split("\n")

    print("\n" + "=" * 60)
    print("✅ AUDIT COMPLETE")
    print("=" * 60)

    # Print first 20 lines as preview
    print("\n📄 Report Preview:\n")
    for line in lines[:20]:
        print(line)

    if len(lines) > 20:
        print(f"\n... ({len(lines) - 20} more lines)")

    print(f"\n💾 Full report saved to: {filepath}")
    print("=" * 60 + "\n")


