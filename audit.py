def parse_args() -> argparse.Namespace:
    """Parse command-line arguments."""
    parser = argparse.ArgumentParser(
        prog="repo-audit-agent",
        description=(
            "Analyze GitHub repositories and generate technical audit reports "
            "using Hermes Agent."
        ),
        formatter_class=argparse.RawDescriptionHelpFormatter,
        epilog="""
Examples:
  python3 audit.py https://github.com/NousResearch/hermes-agent
  python3 audit.py https://github.com/owner/repo --output ./my-reports
  python3 audit.py https://github.com/owner/repo --max-turns 20

Requirements:
  - Hermes Agent installed (https://hermes-agent.org)
  - A valid LLM API key configured in ~/.hermes/.env
        """,
    )

    parser.add_argument(
        "repo_url",
        help="GitHub repository URL to analyze (e.g. https://github.com/owner/repo)",
    )

    parser.add_argument(
        "--output", "-o",
        type=Path,
        default=DEFAULT_OUTPUT_DIR,
        metavar="DIR",
        help=f"Directory to save reports (default: {DEFAULT_OUTPUT_DIR})",
    )

    parser.add_argument(
        "--max-turns",
        type=int,
        default=15,
        metavar="N",
        help="Maximum Hermes Agent tool-calling iterations (default: 15)",
    )

    parser.add_argument(
        "--timeout",
        type=int,
        default=300,
        metavar="SECONDS",
        help="Maximum seconds to wait for Hermes Agent (default: 300)",
    )

    parser.add_argument(
        "--version", "-v",
        action="version",
        version=f"repo-audit-agent {VERSION}",
    )

    return parser.parse_args()


def validate_repo_url(url: str) -> None:
    """
    Validate that the provided URL is a valid GitHub repository URL.

    Args:
        url: URL to validate

    Raises:
        ValueError: If the URL is not a valid GitHub repository URL
    """
    if not url.startswith("https://github.com/"):
        raise ValueError(
            f"Invalid URL: {url}\n"
            "Please provide a valid GitHub repository URL "
            "(e.g. https://github.com/owner/repo)"
        )

    parts = url.rstrip("/").split("/")
    if len(parts) < 5:
        raise ValueError(
            f"Invalid GitHub URL: {url}\n"
            "URL must include both owner and repository name "
            "(e.g. https://github.com/owner/repo)"
        )


# ---------------------------------------------------------------------------
# Main Entry Point
# ---------------------------------------------------------------------------

def main() -> int:
    """
    Main entry point for repo-audit-agent.

    Returns:
        Exit code (0 = success, 1 = error)
    """
    args = parse_args()

    # Validate input
    try:
        validate_repo_url(args.repo_url)
    except ValueError as e:
        print(f"❌ Error: {e}", file=sys.stderr)
        return 1

    repo_name = args.repo_url.rstrip("/").split("/")[-1]

    # Run the audit using Hermes Agent
    try:
        report = run_hermes_audit(
            repo_url=args.repo_url,
            max_turns=args.max_turns,
            timeout=args.timeout,
        )
    except subprocess.TimeoutExpired:
        print(
            f"❌ Error: Hermes Agent timed out after {args.timeout} seconds.\n"
            "Try increasing --timeout or --max-turns.",
            file=sys.stderr,
        )
        return 1
    except RuntimeError as e:
        print(f"❌ Error: {e}", file=sys.stderr)
        return 1
    except FileNotFoundError:
        print(
            "❌ Error: 'hermes' command not found.\n"
            "Please install Hermes Agent: https://hermes-agent.org",
            file=sys.stderr,
        )
        return 1

    # Save and display results
    filepath = save_report(report, repo_name, args.output)
    print_summary(report, filepath)

    return 0


if __name__ == "__main__":
    sys.exit(main())
